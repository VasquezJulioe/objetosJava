/**
 * @author Julio Vasquez
 * represents objects of class Cellphone
 */
public class Cellphone {
    /**
     * represents the private number attribute
     */
    private int number;
    /**
     * represents the public mark attribute
     */
    public String mark;
    /**
     * represents the protected pin attribute
     */
    protected int pin;
    /**
     * represents the public color attribute
     */

    public String color;
    /**
     * getter of number
     * @return number
     */
    public int getNumber() {
        return number;
    }
    /**
     * setter of number
     * @param number
     */
    public void setNumber(int number) {
        this.number = number;
    }

    /**
     * getter of mark
     * @return mark
     */
    public String getMark() {
        return mark;
    }

    /**
     * setter of mark
     * @param mark
     */
    public void setMark(String mark) {
        this.mark = mark;
    }

    /**
     * getter of pin
     * @return pin
     */
    public int getPin() {
        return pin;
    }
    /**
     * setter of pin
     * @param pin
     */
    public void setPin(int pin) {
        this.pin = pin;
    }
    /**
     * getter of color
     * @return color
     */
    public String getColor() {
        return color;
    }
    /**
     * getter of color
     * @return color
     */
    public void setColor(String color) {
        this.color = color;
    }
}
